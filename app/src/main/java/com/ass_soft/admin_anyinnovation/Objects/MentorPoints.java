package com.ass_soft.admin_anyinnovation.Objects;

public class MentorPoints {
    private String id,email,n_comments,n_brain;

    public MentorPoints() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getN_comments() {
        return n_comments;
    }

    public void setN_comments(String n_comments) {
        this.n_comments = n_comments;
    }

    public String getN_brain() {
        return n_brain;
    }

    public void setN_brain(String n_brain) {
        this.n_brain = n_brain;
    }
}
