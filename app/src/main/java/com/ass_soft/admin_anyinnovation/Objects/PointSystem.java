package com.ass_soft.admin_anyinnovation.Objects;

public class PointSystem {
    private String id, braindate, 
            message, mentor, pro_mentor;

    public PointSystem() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getBraindate() {
        return braindate;
    }

    public void setBraindate(String braindate) {
        this.braindate = braindate;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    public String getMentor() {
        return mentor;
    }

    public void setMentor(String mentor) {
        this.mentor = mentor;
    }

    public String getPro_mentor() {
        return pro_mentor;
    }

    public void setPro_mentor(String pro_mentor) {
        this.pro_mentor = pro_mentor;
    }
}
